<h4>Rules</h4>
<br>
	<ul>
	<li>Each participant must compete as an individual (no teams allowed)</li>
	<li>Competition lasts 4 hours</li>
	<li>Problem set consists of 6-10 problems, testing algorithm-based knowledge</li>
	<li>Solutions may be submitted in Java, C/C++, C#, Python, or Ruby</li>
	<li>All problems are read from standard in and printed to standard out</li>
	<li>Traditional ACM-style scoring</li>
		<ul> 
		<li>Winners determined by number of problems solved</li>
		<li>Ties are broken by penalty points</li>
			<ul>
			<li>You earn one penalty point for each minute from the beginning of the contest a correct solution was submitted</li>
			<li>You earn 20 penalty points for each incorrect submission (only solved problems count toward total)</li>
			<li>Any submissions after a correct submission will not be scored</li>
			<li>E.g. Person A submits problem A 20 minutes into the contest on his first try, then submits two submissions for problem B, the latter being correct 40 minutes after problem A (or 60 minutes from the start of the competition), and submitted an incorrect solution for problem C. He then has 2 correct problems and 20 (A) + (60 + 20) (B) for a total of 100 penalty points</li>
			</ul>
			<li>Any additional ties will be broken by an intense arm-wrestling match.</li>
		</ul>
	<li>In all seriousness ,after submitting your solution, you will get one of the following responses:</li>
		<ul>
		<li>Compilation Error – your solution failed to compile on our server</li>
		<li>Run-Time Error – this response means something caused the program to stop running (index out of bounds, division by 0, etc.)</li>
		<li>Time-Limit Exceeded – this means your program took longer than the time allotted for your program to run</li>
		<li>Wrong Answer – your program finished in the correct time limit without errors, but the answers are incorrect or in an incorrect format</li>
		<li>Correct – your solution finished in the time limit without errors, and your output matched the judge’s output</li>
		</ul>
	<li>The response you get will be the first one from this list that applies. For instance, if your program generated some output for the first few test cases, but then threw an exception, your response will be Run-Time error, even if the output was incorrect</li>
	</ul>
